volatile int valueOfRegister;
volatile int isFlag = 0;

void ADC1_IRQHandler(void)
{
	if (ADC1->SR & ADC_SR_EOC)
 	{
 		valueOfRegister = ADC1->DR;
 	}
}

void USART2_IRQHandler(void)
{
	uint8_t pom = 0;
	if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)
	{
		USART_ClearITPendingBit(USART2, USART_IT_RXNE);
		pom = USART_ReceiveData(USART2);
		// ak prijimeme m-ko
		if(pom == 'm')
		{
			if(isFlag == 0){
				isFlag = 1;
			}
			else{
				isFlag = 0;
			}
		}
	}
}

void Vypis()
{
	int i=0;
	char c[5] = "abcde";
	sprintf(c, "%d", valueOfRegister);
	while(c[i] != '\0')
	{

	  	USART_SendData(USART2, c[i]);
	  	while (USART_GetFlagStatus(USART2, USART_FLAG_TC) == RESET);
	  	i++;
	}

	USART_SendData(USART2, '_');
	while (USART_GetFlagStatus(USART2, USART_FLAG_TC) == RESET);
}

void ConvertAndPrint(int aktualnaHodnota)
{
	int i = 0;
	char c[4] = "abcd";
	int prepocitane = (33*aktualnaHodnota)/4096;
	sprintf(c, "%d", prepocitane);
	if (c[1] == '\0')
	{
		USART_SendData(USART2, '0');
		while (USART_GetFlagStatus(USART2, USART_FLAG_TC) == RESET);
		USART_SendData(USART2, 'V');
		while (USART_GetFlagStatus(USART2, USART_FLAG_TC) == RESET);
	}
	while(c[i] != '\0')
	{
		if(i == 1)
		{
			USART_SendData(USART2, 'V');
			while (USART_GetFlagStatus(USART2, USART_FLAG_TC) == RESET);
			USART_SendData(USART2, c[i]);
			while (USART_GetFlagStatus(USART2, USART_FLAG_TC) == RESET);
		}
	  	else
	  	{
	  		USART_SendData(USART2, c[i]);
	  		while (USART_GetFlagStatus(USART2, USART_FLAG_TC) == RESET);
	  	}
		i++;
	}
	USART_SendData(USART2, '_');
	while (USART_GetFlagStatus(USART2, USART_FLAG_TC) == RESET);
}


